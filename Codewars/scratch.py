def pattern(n):
    if n < 1:
        return ''

    units = get_units(n)

    results = [create_initial_list(units, n)]

    for row_i in range(n):

        row_list = results[row_i][:]

        decrease_list_items(row_i, row_list)

        results.append(row_list)

    results.pop(0)

    return '\n'.join([''.join([str(value) for value in row]) for row in results])


def create_initial_list(units, n):
    return [units + 1 for i in range(n)]


def get_units(number):
    str_number = str(number)
    return int(str_number[len(str_number) - 1])


def decrease_list_items(start_index, arr):
    for i in range(start_index, len(arr)):
        new_value = arr[i] - 1
        if new_value < 0:
            arr[i] = 9
        else:
            arr[i] = new_value


